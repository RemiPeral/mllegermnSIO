        <h1>Bios</h1>
        <p><h4>Baroudeurs infatigables des grands espaces musicaux, leurs chemins se sont croisés à l’orée d’un kiosque où ce soir là on ne jouait pas Mozart. Une rencontre …un destin ! La passion commune qu’ils se découvrent en cet instant pour Brel a fait le reste. D’autant qu’une chanteuse, un accordéoniste et un percussionniste, il y a à première vue de quoi faire quelque chose ensemble. Non ?</h4></p>
        <span class="center"><img src="style/groupe1.jpg" alt="example graphic"><img src="style/groupe2.jpg" alt="example graphic"></span>
	
		<p><h4>La petite graine semée est aujourd’hui devenue le groupe musical Mlle GermN. Un groupe mature composé de musiciens aussi loufoques que rigoureux et qui joue un Jacques Brel bien à lui, à la musique relookée et roborative.
Mais qui sont nos trois lascars ?<h4></p>
<div id = "jeane">
        <ul><li><h4>Chant : Jeane</h4></li>

		<p>Auteur compositeur reconnu, l’authenticité de sa voix et la sincérité de ses textes reflètent son émotion devant des sujets de société: le Ministère de la Solidarité lui accorde son label en 1997 pour ses chansons sur l’enfance maltraitée.</p>
<p>Dès 1987, que ce soit en première partie de Patty Layne, au Festival de Saint Pierre de Chartreux ou ensore dans les salles, partout les critiques sont élogieuses. Il faut dire que formée à l’intransigeante école du cabaret, elle exulte sur scène car par-dessus tout elle aime partager ses passions avec le public.</p>
          
		  
		  <span class="center"><img src="style/jeane1.jpg" alt="example graphic"><img src="style/jez.jpg" alt="example graphic"></span>
		  <p>Elle aime les personnages forts et les chansons à texte. Sa passion pour Brel coule de source. Elle le chante depuis longtemps avec une voix pleine de générosité et de fantaisie joyeuse qui exhale ses souvenirs d’enfance en Afrique du Nord dont elle a gardé l’empreinte du soleil, le goût de la fête… et de la musique.</p>
<p>Chanteuse au fort charisme, elle est l’âme du groupe et l’instigatrice du projet de Mlle GermN.</p>
		  </div>
		  <div id = "patrick">
		  <li><h4>Accordéon : Patrick</h4></li>
		  <p>A 40 ans, ce professeur d’accordéon diplômé d’état s’il vous plait dispose d’une solide formation musicale. En 1992 il remporte le Trophée Joss Baselli à Lille dont le président du jury est Marcel Azzola, le fameux « chauffe Marcel » de Vesoul. Un signe ?</p>
<p>Depuis, son parcours foisonne d’expériences multiples. Un jour il joue dans un opéra rock «L’œil du Monde», le lendemain il enregistre un CD de jazz et il peut jouer le surlendemain avec un quatuor Tango. Sans oublier bien sûr son rôle essentiel dans de grands orchestres.</p>
		  
		  <span class="center"><img src="style/patrick1.jpg" alt="example graphic"><img src="style/patrick2.jpg" alt="example graphic"></span>
		  
		  <p>Ces dernières années il a joué en Chine, au Brésil et un peu partout en Europe. Oui, c’est une pointure le Patrick. Avec lui au sein de Mlle GermN, l’accordéon retrouve ses lettres de noblesse… populaires.</p>
